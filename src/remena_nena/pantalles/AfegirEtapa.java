    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nom = nomEtapatext.getText();
        if (nom.length() > 0 && finEt.getEmesos().length > 0) {
            Controlador k = finEt.getK();
            // PRE la controlem a FinEt
            finEt.setNomEtapa(nom);
            if (finEt.afegir()) {
                this.setVisible(false);
                FinestraInicial ini = new FinestraInicial(k);
                Inicial finIni = new Inicial(ini);
                finIni.setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(this, "No es pot repetir el mateix nom de Campanya","ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

