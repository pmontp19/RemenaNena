    private void dacordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dacordActionPerformed
        boolean isInt;
        int clics = -1;
        try {
            clics = Integer.parseInt(clicsField.getText());
            isInt = true;
        } catch (NumberFormatException e) {
            isInt = false;
        }
        if (isInt && clics > 0) {
            finCon.setClicsCondicio(clics);
            if (relativaRadio.isSelected() && !etapesList.isSelectionEmpty() && !accionsList.isSelectionEmpty()
                && !memesList.isSelectionEmpty()) {
                finCon.setEtapaActiva(etapesList.getSelectedValue());
                int accio = Integer.parseInt(accionsList.getSelectedValue());
                finCon.setIdAccio(accio);
                finCon.setIdMeme(memesList.getSelectedValue());
                finCon.acceptarRelativa();
                this.setVisible(false);
                FinestraInicial p = new FinestraInicial(finCon.getK());
                Inicial ini = new Inicial(p);
                ini.setVisible(true);
            } else if (absolutaRadio.isSelected() && !etapesList1.isSelectionEmpty() && !accionsList1.isSelectionEmpty()) {
                finCon.setEtapaActiva(etapesList1.getSelectedValue());
                int accio = Integer.parseInt(accionsList1.getSelectedValue());  
                finCon.setIdAccio(accio);
                finCon.acceptarAbsoluta();
                this.setVisible(false);
                FinestraInicial p = new FinestraInicial(finCon.getK());
                Inicial ini = new Inicial(p);
                ini.setVisible(true);
            }
        }
    }//GEN-LAST:event_dacordActionPerformed

